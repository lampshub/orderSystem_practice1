package com.beyond23.orderSystem.common.configs;


import com.beyond23.orderSystem.common.auth.JwtTokenFilter;
import com.beyond23.orderSystem.common.exception.JwtAuthenticationHandler;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.security.config.annotation.method.configuration.EnableMethodSecurity;
import org.springframework.security.config.annotation.web.builders.HttpSecurity;
import org.springframework.security.config.annotation.web.configurers.AbstractHttpConfigurer;
import org.springframework.security.config.annotation.web.configurers.CorsConfigurer;
import org.springframework.security.config.http.SessionCreationPolicy;
import org.springframework.security.crypto.factory.PasswordEncoderFactories;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.security.web.SecurityFilterChain;
import org.springframework.security.web.authentication.UsernamePasswordAuthenticationFilter;
import org.springframework.web.cors.CorsConfiguration;
import org.springframework.web.cors.CorsConfigurationSource;
import org.springframework.web.cors.UrlBasedCorsConfigurationSource;

import java.util.Arrays;

//filterChain
@Configuration
@EnableMethodSecurity   //PreAuthorize어노테이션을 사용하기 위한 설정 (Controller에서 사용)
public class SecurityConfig {
    private final JwtTokenFilter jwtTokenFilter;
    private final com.beyond23.orderSystem.common.exception.JwtAuthenticationHandler jwtAuthenticationHandler;

    @Autowired
    public SecurityConfig(JwtTokenFilter jwtTokenFilter, JwtAuthenticationHandler jwtAuthenticationHandler) {
        this.jwtTokenFilter = jwtTokenFilter;
        this.jwtAuthenticationHandler = jwtAuthenticationHandler;
    }

//    내가 만든 클래스와 객체는 @Component, 외부 클래스(라이브러리)를 활용한 객체는 @Configuration+@Bean
    @Bean
    public SecurityFilterChain filterChain(HttpSecurity httpSecurity) throws Exception {
        return httpSecurity
                .cors(c->c.configurationSource(corsConfigurationSource()))
//                csrf공격(일반적으로 쿠키(세션)를 활용한 공격)에 대한 방어 비활성화
                .csrf(AbstractHttpConfigurer::disable)  // 클래스명::메서드명 => 메서드참조 a->sout(a) s::out
//                http basic은 email/pw를 인코딩하여 인증(전송)하는 간단한 인증방식. 비활성화
                .httpBasic(AbstractHttpConfigurer::disable)
//                세션로그인방식 비활성화 (stateless방식)
                .sessionManagement(a->a.sessionCreationPolicy(SessionCreationPolicy.STATELESS))
//                token을 검증하고, Authentication 객체 생성 => jwtTokenFilter실행 (순서는 Filter->Security->Controller)
                .addFilterBefore(jwtTokenFilter, UsernamePasswordAuthenticationFilter.class)
                .exceptionHandling(e->e.authenticationEntryPoint(jwtAuthenticationHandler))
//                지정한 특정 url을 제외한 모든 요청에 대해서 authenticated(인증처리)하겠다 라는 의미   //filter체인으로 돌아오면 여기
                .authorizeHttpRequests(a->a.requestMatchers("/member/create","/member/doLogin").permitAll().anyRequest().authenticated())
                .build();
    }

    public CorsConfigurationSource corsConfigurationSource(){
        CorsConfiguration configuration = new CorsConfiguration();
//        허용가능한 도메인 목록 설정 **중요코드
        configuration.setAllowedOrigins(Arrays.asList("http://localhost:3000", "https://www.lamps.shop"));
//        모든 HTTP메서드(GET, POST, OPTIONS 등) 허용
        configuration.setAllowedMethods(Arrays.asList("*"));
//        모든 header 요소를 허용
        configuration.setAllowedHeaders(Arrays.asList("*"));
//         자격 증명 허용
        configuration.setAllowCredentials(true);

        UrlBasedCorsConfigurationSource source = new UrlBasedCorsConfigurationSource();
//        모든 url패턴에 대해 위 cors 정책을 적용
        source.registerCorsConfiguration("/**", configuration);
        return source;
    }

    @Bean
    public PasswordEncoder pwEncoder(){
        return PasswordEncoderFactories.createDelegatingPasswordEncoder();
    }
}
